    def _add_tabs(self, tikz_script: str) -> str:
        """Indent a TikZ script for improved readability.

        Increases indentation after ``\\begin`` commands and decreases it
        before ``\\end`` commands.

        Args:
            tikz_script: Raw TikZ source string.

        Returns:
            The same source with each line indented by four spaces per
            nesting level.
        """
        tikz_script_new = ""
        tab_str = "    "
        num_tabs = 0
        for line in tikz_script.split("\n"):
            if "\\end" in line or "end \\foreach" in line:
                num_tabs = max(num_tabs - 1, 0)
            tikz_script_new += f"{tab_str * num_tabs}{line}".rstrip() + "\n"
            if "\\begin" in line or "start \\foreach" in line:
                num_tabs += 1
            if "\\foreach" in line and "in" in line and "{" in line:
                num_tabs += 1
            # if "} % \\end foreach" in line and num_tabs > 0:
            #     num_tabs -= 1
        return tikz_script_new

    def _render_axis_in_groupplot(self, axis: Axis2D) -> str:
        """Render an Axis2D as a \\nextgroupplot entry inside a groupplot."""
        axis_tikz = axis.to_tikz()
        axis_tikz = axis_tikz.replace("\\begin{tikzpicture}\n", "")
        axis_tikz = axis_tikz.replace("\\end{tikzpicture}", "")
        axis_tikz = axis_tikz.strip()

        # Replace \begin{axis}[ with \nextgroupplot[
        axis_tikz = axis_tikz.replace("\\begin{axis}[", "\\nextgroupplot[")

        # Check if axis has function-based plots and add domain if needed
        has_function = any(plot.is_function for plot in axis.plots)
        if has_function and axis.xlim:
            xmin, xmax = axis.xlim
            axis_tikz = axis_tikz.replace(
                "\\nextgroupplot[",
                f"\\nextgroupplot[domain={xmin}:{xmax}, ",
                1,
            )

        # Remove the \end{axis} (groupplot handles it)
        axis_tikz = axis_tikz.replace("\\end{axis}", "")

        # Indent and add to groupplot
        axis_lines = axis_tikz.split("\n")
        return "\n".join(["    " + line for line in axis_lines]) + "\n"

    def _render_groupplot_grid(self, num_rows: int, num_cols: int) -> str:
        """Render a grid of Axis2D items using pgfplots groupplot."""
        group_opts = (
            f"group style={{"
            f"group size={num_cols} by {num_rows}, "
            f"horizontal sep=1.5cm, "
            f"vertical sep=2cm"
            f"}}"
        )
        result = f"\\begin{{groupplot}}[{group_opts}]\n"

        for row in range(num_rows):
            for col in range(num_cols):
                if (row, col) in self._subfigure_grid:
                    item_data = self._subfigure_grid[(row, col)]
                    axis = item_data[0]
                    result += self._render_axis_in_groupplot(axis)

        result += "\\end{groupplot}\n"
        return result

    def _render_mixed_grid(self, num_rows: int, num_cols: int) -> str:
        """Render a grid with mixed Axis2D and bare TikzFigure items.

        Uses scope-based positioning instead of groupplot, since groupplot
        always creates axis environments which is wrong for bare diagrams.
        """
        textwidth_cm = 14.0  # approximate \\textwidth
        h_sep = 2.0  # cm between columns
        v_sep = 2.0  # cm between rows
        default_height = 6.0  # cm

        # Compute column widths (max width per column)
        col_widths: list[float] = []
        for col in range(num_cols):
            max_w = 0.0
            for row in range(num_rows):
                if (row, col) in self._subfigure_grid:
                    w = self._subfigure_grid[(row, col)][1]
                    max_w = max(max_w, w * textwidth_cm)
            col_widths.append(max_w if max_w > 0 else 7.0)

        # Compute cumulative x-offsets per column
        x_offsets = [0.0]
        for c in range(num_cols - 1):
            x_offsets.append(x_offsets[-1] + col_widths[c] + h_sep)

        # Compute row heights (max height per row)
        row_heights: list[float] = []
        for row in range(num_rows):
            max_h = default_height
            for col in range(num_cols):
                if (row, col) in self._subfigure_grid:
                    item_data = self._subfigure_grid[(row, col)]
                    if len(item_data) == 3 and item_data[2] != "None":
                        h_str = str(item_data[2])
                        if h_str.endswith("cm"):
                            max_h = max(max_h, float(h_str[:-2]))
            row_heights.append(max_h)

        # Compute cumulative y-offsets per row (negative = downward)
        y_offsets = [0.0]
        for r in range(num_rows - 1):
            y_offsets.append(y_offsets[-1] - row_heights[r] - v_sep)

        # Render each cell in a positioned scope
        result = ""
        for row in range(num_rows):
            for col in range(num_cols):
                if (row, col) not in self._subfigure_grid:
                    continue

                item_data = self._subfigure_grid[(row, col)]
                if len(item_data) == 3:
                    item, width, height_str = item_data
                else:
                    item, width = item_data
                    height_str = "None"

                x = x_offsets[col]
                y = y_offsets[row]
                width_cm = f"{width * textwidth_cm:.1f}cm"

                if isinstance(item, Axis2D):
                    result += f"\\begin{{scope}}[xshift={x:.1f}cm, yshift={y:.1f}cm]\n"
                    axis_tikz = item.to_tikz()
                    # Add width to axis if not already set
                    if not item._width:
                        axis_tikz = axis_tikz.replace(
                            "\\begin{axis}[",
                            f"\\begin{{axis}}[width={width_cm}, ",
                        )
                    # Add domain for function-based plots
                    has_function = any(plot.is_function for plot in item.plots)
                    if has_function and item.xlim:
                        xmin, xmax = item.xlim
                        axis_tikz = axis_tikz.replace(
                            "\\begin{axis}[",
                            f"\\begin{{axis}}[domain={xmin}:{xmax}, ",
                            1,
                        )
                    for line in axis_tikz.split("\n"):
                        if line.strip():
                            result += f"    {line}\n"
                    result += "\\end{scope}\n"
                elif isinstance(item, TikzFigure):
                    result += f"\\begin{{scope}}[xshift={x:.1f}cm, yshift={y:.1f}cm]\n"
                    for layer_label, layer in item._layers.layers.items():
                        if isinstance(layer_label, int):
                            for tikz_obj in layer.items:
                                tikz_content = tikz_obj.to_tikz()
                                for line in tikz_content.split("\n"):
                                    if line.strip():
                                        result += f"    {line}\n"
                    result += "\\end{scope}\n"

        return result

    def generate_tikz(
        self,
        use_layers: bool = True,
        skip_header: bool = False,
        verbose: bool = False,
    ) -> str:
        """Generate the complete TikZ source for this figure.

        Args:
            use_layers: If ``True`` and the figure has more than one layer,
                wrap each layer in a ``pgfonlayer`` environment. Defaults
                to ``True``.
            skip_header: If ``True``, omit the TikZ header. Defaults to ``False``.
            verbose: If ``True``, print debug information during generation.

        Returns:
            A string containing the full TikZ source, starting with
            ``\\begin{tikzpicture}`` and ending with
            ``\\end{tikzpicture}``. Wrapped in a ``figure`` environment
            when a label is set.
        """
        # Ensure pgfplots is in extra_packages if axes are present
        if self.axes and "pgfplots" not in (self._extra_packages or []):
            if self._extra_packages is None:
                self._extra_packages = []
            self._extra_packages.append("pgfplots")

        tikz_script = ""
        if not skip_header:
            tikz_script += TIKZFIGURE_HEADER
        tikz_script += "\\begin{tikzpicture}\n"
        if self._figure_setup:
            tikz_script += f"[{self._figure_setup}]\n"

        # Add variables
        if len(self.variables) > 0:
            for variable in self.variables:
                tikz_script += (
                    f"\\pgfmathsetmacro{{\\{variable.label}}}{{{variable.value}}}\n"
                )

        # Add colors
        if len(self.colors) > 0:
            for name, color in self.colors:
                tikz_script += f"\\colorlet{{{name}}}{{{color.color_spec}}}\n"

        # Add 3D axis
        if self.ndim == 3:
            tikz_script += "\\begin{axis}[\n"
            tikz_script += "view={20}{30},\n"
            if self._show_axes:
                tikz_script += "axis lines=center,\n"
                tikz_script += "xlabel={$x$},\n"
                tikz_script += "ylabel={$y$},\n"
                tikz_script += "zlabel={$z$},\n"
                tikz_script += "grid=major\n"
            else:
                tikz_script += "hide axis\n"
            tikz_script += "    ]\n"

        # Collect all layer keys from both drawing elements and axes
        all_layer_keys = set(self.layers.layers.keys())
        for axis in self.axes:
            axis_layer = axis.layer if axis.layer is not None else 0
            all_layer_keys.add(axis_layer)

        if len(all_layer_keys) <= 1:
            use_layers = False

        # TODO: self.layers.layers is a bit clunky
        layers = sorted([str(lyr) for lyr in all_layer_keys])
        if use_layers:
            tikz_script += "% Define the layers library\n"
            for lyr_str in layers:
                tikz_script += f"\\pgfdeclarelayer{{{lyr_str}}}\n"
            if len(layers) > 0:
                tikz_script += f"\\pgfsetlayers{{{','.join(layers)}}}\n"

        # Add grid if enabled
        # TODO: Create a Grid class
        if self._grid:
            tikz_script += (
                "    \\draw[step=1cm, gray, very thin] (-10,-10) grid (10,10);\n"
            )
        ordered_layers = []
        buffered_layers = set()

        for key, layer in self.layers.layers.items():
            # layer_order, buffered_layers = update_layer_order(layer, layer_order, buffered_layers)
            reqs = layer.get_reqs()
            if all([r == layer.label for r in reqs]):
                ordered_layers.append(layer)
            elif all([r in [layer.label for layer in ordered_layers] for r in reqs]):
                ordered_layers.append(layer)
            else:
                buffered_layers.add(layer)

            for buffered_layer in list(buffered_layers):
                buff_reqs = buffered_layer.get_reqs()
                if all(
                    [r in [layer.label for layer in ordered_layers] for r in buff_reqs],
                ):
                    # print("Move layer from buffer")
                    ordered_layers.append(buffered_layer)
                    buffered_layers.remove(buffered_layer)
        assert len(buffered_layers) == 0, (
            "Layer order is impossible for layer"
            + f"{[layer.label for layer in buffered_layers]}"
        )
        # Group axes by layer for proper ordering
        axes_by_layer: dict[int, list[Axis2D]] = {}
        for axis in self.axes:
            layer_key = axis.layer if axis.layer is not None else 0
            if layer_key not in axes_by_layer:
                axes_by_layer[layer_key] = []
            axes_by_layer[layer_key].append(axis)

        # Render layers and axes together (axes for each layer after that layer)
        for layer in ordered_layers:
            tikz_script += layer.generate_tikz(use_layers=use_layers, verbose=verbose)
            # Render axes for this layer immediately after the layer itself
            if isinstance(layer.label, int) and layer.label in axes_by_layer:
                for axis in axes_by_layer[layer.label]:
                    tikz_script += axis.to_tikz()

        # Render any axes on layers that weren't explicitly created (e.g., axes only on layer 5)
        rendered_layers = {layer.label for layer in ordered_layers}
        for layer_key in sorted(axes_by_layer.keys()):
            if layer_key not in rendered_layers:
                # Axes on a layer with no other content - wrap in pgfonlayer if needed
                for axis in axes_by_layer[layer_key]:
                    if use_layers:
                        tikz_script += f"\n% Layer {layer_key}\n"
                        tikz_script += f"\\begin{{pgfonlayer}}{{{layer_key}}}\n"
                        tikz_script += axis.to_tikz()
                        tikz_script += f"\\end{{pgfonlayer}}{{{layer_key}}}\n"
                    else:
                        tikz_script += axis.to_tikz()

        if self.ndim == 3:
            tikz_script += "\\end{axis}\n"
        tikz_script += "\\end{tikzpicture}"

        # Handle subfigure layout if subfigure axes are present
        if self._subfigure_axes or self._subfigure_grid:
            # Create a single tikzpicture using pgfplots groupplot environment
            # for proper layout management
            subfig_tikz = "\\begin{tikzpicture}\n"
            if self._figure_setup:
                subfig_tikz = subfig_tikz.replace(
                    "\\begin{tikzpicture}\n",
                    f"\\begin{{tikzpicture}}[{self._figure_setup}]\n",
                )

            # Add variables and colors (shared across all axes)
            for variable in self.variables:
                subfig_tikz += (
                    f"\\pgfmathsetmacro{{\\{variable.label}}}{{{variable.value}}}\n"
                )
            for name, color in self.colors:
                subfig_tikz += f"\\colorlet{{{name}}}{{{color.color_spec}}}\n"

            # Determine layout and collect axes to render
            if self._subfigure_rows is not None:  # Grid mode
                num_cols = self._subfigure_cols
                assert num_cols is not None  # Type guard for mypy
                num_rows = self._subfigure_rows

                # Check if grid has any bare TikzFigure items (diagrams)
                has_bare_subfigures = any(
                    isinstance(item_data[0], TikzFigure)
                    for item_data in self._subfigure_grid.values()
                )

                if has_bare_subfigures:
                    # Mixed content: use scope-based positioning
                    # (groupplot can't render non-axis content)
                    subfig_tikz += self._render_mixed_grid(num_rows, num_cols)
                else:
                    # All Axis2D: use groupplot for proper alignment
                    subfig_tikz += self._render_groupplot_grid(num_rows, num_cols)
            else:  # Single-row mode (backward compatible)
                num_axes = len(self._subfigure_axes)
                group_opts = (
                    f"group style={{group size={num_axes} by 1, horizontal sep=1.5cm}}"
                )
                subfig_tikz += f"\\begin{{groupplot}}[{group_opts}]\n"
                for item_tuple in self._subfigure_axes:
                    item, width = item_tuple
                    subfig_tikz += self._render_axis_in_groupplot(item)
                subfig_tikz += "\\end{groupplot}\n"

            subfig_tikz += "\\end{tikzpicture}"
            tikz_script = subfig_tikz
        # Wrap in figure environment if necessary (and not already wrapped for subfigures)
        elif self._description or self._label:
            figure_env = "\\begin{figure}\n" + tikz_script + "\n"
            if self._label:
                figure_env += f"    \\label{{{self._label}}}\n"
            figure_env += "\\end{figure}"
            tikz_script = figure_env
        tikz_script = self._add_tabs(tikz_script)

        # Cleanup
        tikz_script = tikz_script.replace(r"% {\end foreach}", "")
        return tikz_script

    @staticmethod
    def generate_subfigures(
        figures: list["TikzFigure"],
        labels: list[str] | None = None,
        widths: list[float] | None = None,
        spacing: str = "0.5cm",
    ) -> str:
        """Generate LaTeX code for side-by-side subfigures.

        Creates multiple figures arranged horizontally in a single LaTeX figure
        environment. Each subfigure can have its own label.

        Args:
            figures: List of TikzFigure objects to arrange side-by-side.
            labels: Optional list of LaTeX labels (one per figure). If provided,
                must have same length as figures.
            widths: Optional list of subfigure widths as fractions (e.g., [0.45, 0.45]).
                If None, width is calculated as (1 - spacing) / num_figures.
            spacing: Horizontal spacing between subfigures. Defaults to "0.5cm".

        Returns:
            LaTeX code for a figure environment with side-by-side subfigures.

        Raises:
            ValueError: If labels length doesn't match figures length.
        """
        if not figures:
            raise ValueError("Must provide at least one figure")

        if labels is not None and len(labels) != len(figures):
            raise ValueError(
                f"labels length ({len(labels)}) must match figures length "
                f"({len(figures)})"
            )

        if widths is not None and len(widths) != len(figures):
            raise ValueError(
                f"widths length ({len(widths)}) must match figures length "
                f"({len(figures)})"
            )

        # Calculate widths if not provided
        if widths is None:
            # Leave room for spacing between figures
            num_figs = len(figures)
            available_width = 1.0 - (num_figs - 1) * 0.05  # 0.05 fraction per spacing
            width_each = available_width / num_figs
            widths = [width_each] * num_figs

        # Build subfigures
        latex_code = "\\begin{figure}\n"

        for i, fig in enumerate(figures):
            width = widths[i]
            label = labels[i] if labels else ""

            # Generate TikZ code without header or figure wrapper
            tikz_code = fig.generate_tikz(skip_header=True)

            # Remove outer figure environment if present (to avoid nesting)
            tikz_code = tikz_code.replace("\\begin{figure}", "").replace(
                "\\end{figure}", ""
            )

            # Start subfigure
            latex_code += f"    \\begin{{subfigure}}{{{width}\\textwidth}}\n"
            latex_code += f"        \\centering\n"
            latex_code += f"        {tikz_code}\n"

            # Add label to subfigure if provided
            if label:
                latex_code += f"        \\label{{{label}}}\n"

            latex_code += "    \\end{subfigure}\n"

            # Add spacing between subfigures (not after last one)
            if i < len(figures) - 1:
                latex_code += f"    \\hspace{{{spacing}}}\n"

        latex_code += "\\end{figure}\n"
        return latex_code

    def generate_standalone(
        self, skip_header: bool = False, verbose: bool = False
    ) -> str:
        """Generate a complete standalone LaTeX document for this figure.

        Args:
            skip_header: If ``True``, omit the TikZ header. Defaults to ``False``.
            verbose: Passed through to :meth:`generate_tikz`.

        Returns:
            A full LaTeX document string (``documentclass`` … ``document``)
            that can be compiled directly with ``pdflatex``.
        """
        tikz_code = self.generate_tikz(skip_header=skip_header, verbose=verbose)

        # Create a minimal LaTeX document
        latex_document = (
            "\\documentclass[border=10pt]{standalone}\n"
            "\\usepackage{tikz}\n"
            "\\usepackage{pgfplots}\n"
            "\\pgfplotsset{compat=newest}\n"
            "\\usepgfplotslibrary{groupplots}\n"
            "\\usetikzlibrary{arrows.meta}\n"
        )
        if self._extra_packages:
            for pkg in self._extra_packages:
                latex_document += f"\\usepackage{{{pkg}}}\n"

        if self.document_setup:
            latex_document += "% Custom document setup\n"
            latex_document += f"{self.document_setup}\n"

        latex_document += f"\\begin{{document}}\n{tikz_code}\n\\end{{document}}"
        return latex_document

    def compile_pdf(
        self,
        filename: Path | str = Path("output.pdf"),
        verbose: bool = False,
        use_web_compilation: bool = False,
    ) -> None:
        """Compile the figure to a PDF file using ``pdflatex`` or web API.

        By default, uses local ``pdflatex`` if available, falling back to the
        web-based LaTeX compilation API if pdflatex fails.

        Requires ``pdflatex`` to be installed (for local compilation) or
        internet access (for web compilation).

        Args:
            filename: Output PDF path. Defaults to ``"output.pdf"``.
            verbose: If ``True``, print the LaTeX source and the
                ``pdflatex`` command before running.
            use_web_compilation: If ``True``, use the web-based LaTeX
                compilation API directly instead of attempting local pdflatex.
                Defaults to ``False``.
        """
        if isinstance(filename, str):
            filename = Path(filename)

        latex_document = self.generate_standalone()
        if verbose:
            print(latex_document)

        # If explicit web compilation is requested, use it directly
        if use_web_compilation:
            from tikzfigure.core.web_compiler import compile_with_latex_on_http

            compile_with_latex_on_http(latex_document, filename, verbose=verbose)
            return

        # Otherwise, try local pdflatex first
        # Use a temporary directory to store the LaTeX files
        with tempfile.TemporaryDirectory() as tempdir:
            # print(f"{type(tempdir) = } {tempdir = }")
            # tempdir = Path(tempdir)
            # tex_file = tempdir / "figure.tex"
            tex_file = Path(tempdir) / "figure.tex"
            with open(tex_file, "w") as f:
                f.write(latex_document)
            # Run pdflatex
            try:
                # Split the path
                head_tail = (str(filename.parent), filename.name)

                output_directory = head_tail[0]
                jobname = head_tail[1].replace(".pdf", "")
                cmd = [
                    "pdflatex",
                    "-interaction=nonstopmode",
                    "-jobname",
                    f"{jobname}",
                    "-output-directory",
                    f"{output_directory}",
                    str(tex_file),
                ]
                if verbose:
                    print(f"{cmd =}")
                subprocess.run(
                    cmd,
                    cwd=tempdir,
                    check=True,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                )
                # Remove .aux and .log files
                os.remove(filename.with_suffix(".aux"))
                os.remove(filename.with_suffix(".log"))
            except (subprocess.CalledProcessError, FileNotFoundError) as e:
                # Pdflatex failed or not available - attempt fallback to web compilation
                if verbose:
                    if isinstance(e, FileNotFoundError):
                        print(
                            "pdflatex not available or compilation failed, attempting fallback to web API"
                        )
                    else:
                        print(
                            "Local pdflatex compilation failed, attempting fallback to web API..."
                        )
                        if e.stderr:
                            print(e.stderr.decode())
                        else:
                            print(str(e))

                from tikzfigure.core.web_compiler import compile_with_latex_on_http

                try:
                    compile_with_latex_on_http(
                        latex_document, filename, verbose=verbose
                    )
                except RuntimeError as web_error:
                    # Both local and web compilation failed - provide full context
                    print("An error occurred while compiling the LaTeX document:")
                    if isinstance(e, subprocess.CalledProcessError):
                        if e.stderr:
                            print(e.stderr.decode())
                        else:
                            print(str(e))
                    elif isinstance(e, FileNotFoundError):
                        print(f"pdflatex not found: {str(e)}")
                    print(f"\nWeb compilation also failed: {str(web_error)}")
                    raise RuntimeError(
                        f"Local compilation failed. Web compilation fallback also failed: {str(web_error)}"
                    ) from web_error

    def savefig(
        self,
        filename: Path | str,
        dpi: int = 300,
        verbose: bool = False,
        transparent: bool = True,
        use_web_compilation: bool = False,
    ) -> None:
        """Save the figure to a file.

        Supported formats: ``.pdf``, ``.tikz``, ``.png``, ``.jpg``,
        ``.jpeg``.  PNG/JPG output first compiles to a temporary PDF and
        then converts via *pymupdf*.

        Args:
            filename: Destination path, including extension.
            dpi: Resolution for raster output (PNG/JPG).
                Defaults to ``300``.
            verbose: If ``True``, print progress messages.
            transparent: If ``True``, render PNG output with a transparent
                background instead of white.  Only supported for ``.png``;
                ignored for other formats.
            use_web_compilation: If ``True``, use the web-based LaTeX
                compilation API instead of local ``pdflatex``. Defaults to ``False``.

        Raises:
            ValueError: If the file extension is not supported.
        """
        if isinstance(filename, str):
            filename = Path(filename)

        ext = filename.suffix.lower()

        if ext == ".pdf":
            # Direct compile
            self.compile_pdf(
                filename=filename,
                verbose=verbose,
                use_web_compilation=use_web_compilation,
            )
        elif ext == ".tikz":
            if verbose:
                print(f"Saving TikZ code to {filename}")
            tikz_code = self.generate_tikz()
            with open(filename, "w") as f:
                f.write(tikz_code)
        elif ext in [".png", ".jpg", ".jpeg"]:
            import fitz  # PyMuPDF

            # Compile to a temporary PDF first
            with tempfile.TemporaryDirectory() as tempdir:
                temp_pdf = Path(tempdir) / "temp.pdf"

                if verbose:
                    print(f"Compiling TikZ to temporary PDF: {temp_pdf}")

                self.compile_pdf(
                    filename=temp_pdf,
                    verbose=verbose,
                    use_web_compilation=use_web_compilation,
                )

                if verbose:
                    print(f"Converting {temp_pdf} → {filename}")

                # Convert PDF → image
                doc = fitz.open(temp_pdf)
                page = doc[0]
                use_alpha = transparent and ext == ".png"
                pix = page.get_pixmap(dpi=dpi, alpha=use_alpha)
                pix.save(filename)
                doc.close()
        else:
            raise ValueError(f"Unsupported file format: {ext}")

    def _show_matplotlib(
        self,
        dpi: int = 300,
        verbose: bool = False,
        transparent: bool = True,
        use_web_compilation: bool = False,
    ) -> None:
        """Display the figure in a Matplotlib window.

        Args:
            dpi: Resolution for the intermediate PNG. Defaults to ``300``.
            verbose: If ``True``, print a status message.
            transparent: If ``True``, make the background transparent.
            use_web_compilation: If ``True``, use the web-based LaTeX
                compilation API instead of local ``pdflatex``.
                Defaults to ``False``.

        Raises:
            ImportError: If ``matplotlib`` is not installed.
        """
        try:
            import matplotlib.image as mpimg
            import matplotlib.pyplot as plt
        except ImportError:
            raise ImportError(
                "matplotlib is required for this backend. "
                "Install with: pip install matplotlib\n"
                "Or use backend='system' or backend='pillow'"
            )

        if verbose:
            print("Using matplotlib backend for display.")

        with tempfile.TemporaryDirectory() as tempdir:
            temp_png = Path(tempdir) / "temp.png"
            self.savefig(
                filename=temp_png,
                dpi=dpi,
                verbose=verbose,
                use_web_compilation=use_web_compilation,
            )

            img = mpimg.imread(temp_png)

            # Calculate figure size based on image dimensions
            height_width_ratio = img.shape[0] / img.shape[1]

            fig, ax = plt.subplots(figsize=(10, 10 * height_width_ratio))
            ax.imshow(img)
            ax.axis("off")
            plt.tight_layout(pad=0)
            plt.show()

    def _show_system(
        self,
        dpi: int = 300,
        transparent: bool = True,
        verbose: bool = False,
        use_web_compilation: bool = False,
    ) -> None:
        """Display the figure using the OS default image viewer.

        Args:
            dpi: Resolution for the intermediate PNG. Defaults to ``300``.
            verbose: Unused; reserved for future debug output.
            transparent: If ``True``, make the background transparent.
            use_web_compilation: If ``True``, use the web-based LaTeX
                compilation API instead of local ``pdflatex``.
                Defaults to ``False``.
        """
        with tempfile.NamedTemporaryFile(suffix=".png", delete=False) as tmp:
            temp_path = tmp.name

        try:
            self.savefig(
                filename=temp_path,
                dpi=dpi,
                transparent=transparent,
                verbose=verbose,
                use_web_compilation=use_web_compilation,
            )

            # Open with system default viewer
            import platform

            system = platform.system()

            if system == "Darwin":  # macOS
                subprocess.run(["open", temp_path], check=True)
            elif system == "Linux":
                subprocess.run(["xdg-open", temp_path], check=True)
            elif system == "Windows":
                os.startfile(temp_path)  # type: ignore[attr-defined]
            else:
                print(f"Saved figure to: {temp_path}")
                print("Please open manually (unsupported platform for auto-display)")
        except Exception as e:
            print(f"Could not open figure automatically: {e}")
            print(f"Figure saved to: {temp_path}")

    def _show_pillow(
        self,
        dpi: int = 300,
        verbose: bool = False,
        transparent: bool = True,
        use_web_compilation: bool = False,
    ) -> None:
        """Display the figure using PIL/Pillow's built-in image viewer.

        Args:
            dpi: Resolution for the intermediate PNG. Defaults to ``300``.
            verbose: Unused; reserved for future debug output.
            transparent: If ``True``, make the background transparent.
            use_web_compilation: If ``True``, use the web-based LaTeX
                compilation API instead of local ``pdflatex``.
                Defaults to ``False``.

        Raises:
            ImportError: If ``Pillow`` is not installed.
        """
        try:
            from PIL import Image
        except ImportError:
            raise ImportError(
                "Pillow is required for this backend. "
                "Install with: pip install Pillow\n"
                "Or use backend='system' or backend='matplotlib'"
            )

        with tempfile.TemporaryDirectory() as tempdir:
            temp_png = Path(tempdir) / "temp.png"
            self.savefig(
                filename=temp_png,
                dpi=dpi,
                transparent=transparent,
                verbose=verbose,
                use_web_compilation=use_web_compilation,
            )

            img = Image.open(temp_png)
            img.show()

    def show(
        self,
        width: int | None = None,
        height: int | None = None,
        dpi: int = 300,
        verbose: bool = False,
        backend: str = "matplotlib",
        transparent: bool = True,
        use_web_compilation: bool = False,
    ) -> None:
        """Display the figure interactively.

        In Jupyter notebooks the figure is shown inline via IPython's
        ``display``.  In regular Python sessions it is opened using the
        selected *backend*.  Display is suppressed automatically when
        running under pytest or when the ``tikzfigure_NO_SHOW=1``
        environment variable is set.

        Args:
            width: Display width in pixels (Jupyter only).
            height: Display height in pixels (Jupyter only).
            dpi: Resolution used when rendering to a raster image.
                Defaults to ``300``.
            verbose: If ``True``, print compilation details.
            backend: Display backend for non-Jupyter environments.
                One of:

                - ``"matplotlib"`` *(default)* – open in a Matplotlib
                  window.
                - ``"system"`` – open with the OS default image viewer.
                - ``"pillow"`` – open with PIL/Pillow.
            use_web_compilation: If ``True``, use the web-based LaTeX
                compilation API instead of local ``pdflatex``.
                Defaults to ``False``.

        Raises:
            ValueError: If *backend* is not one of the supported values.
        """
        # Skip display in test/headless environments
        if os.environ.get("tikzfigure_NO_SHOW") == "1" or os.environ.get(
            "PYTEST_CURRENT_TEST"
        ):
            if verbose:
                print("Display suppressed (test/headless mode).")
            return

        # Check if we're in a Jupyter/IPython environment
        try:
            from IPython import get_ipython

            if get_ipython() is not None and "IPKernelApp" in get_ipython().config:
                # We're in Jupyter - use IPython display
                from IPython.display import Image, display

                with tempfile.TemporaryDirectory() as tempdir:
                    temp_pdf = Path(tempdir) / "temp.png"
                    self.savefig(
                        filename=temp_pdf,
                        transparent=transparent,
                        verbose=verbose,
                        use_web_compilation=use_web_compilation,
                    )
                    display(Image(filename=temp_pdf, width=width, height=height))
                return
        except (ImportError, AttributeError):
            pass

        # Not in Jupyter - use specified backend
        if backend == "matplotlib":
            self._show_matplotlib(
                dpi=dpi,
                verbose=verbose,
                transparent=transparent,
                use_web_compilation=use_web_compilation,
            )
        elif backend == "system":
            self._show_system(
                dpi=dpi,
                verbose=verbose,
                transparent=transparent,
                use_web_compilation=use_web_compilation,
            )
        elif backend == "pillow":
            self._show_pillow(
                dpi=dpi,
                verbose=verbose,
                transparent=transparent,
                use_web_compilation=use_web_compilation,
            )
        else:
            raise ValueError(
                f"Unknown backend '{backend}'. "
                "Options: 'matplotlib', 'system', 'pillow'"
            )

